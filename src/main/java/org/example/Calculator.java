package org.example;

public class Calculator {
    public void print() {
        System.out.printf("hello");
    }

    public int add(int x, int y) {
        return x + y;
    }

    public int subtract(int x, int y) {
        return x - y;
    }

    public int multiple(int x, int y) {
        return x * y;
    }

    public int divide(int x, int y) {
        return x / y;
    }


}
